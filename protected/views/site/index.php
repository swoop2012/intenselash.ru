       <h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Что такое Карепрост?</h4>
<p>Careprost – офтальмологический (глазной) раствор, с равным успехом применяющийся как в медицине, так и в косметологии. Косметологи рекомендуют данный препарат как средство, заметно улучшающее общее состояние ресниц, увеличивая их длину и густоту.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Что такое гипотрихоз ресниц?</h4>
<p>Гипотрихоз представляет собой заболевание, характеризующееся недоразвитием, выпадением или плохим состоянием ресниц. Причинами такого состояния могут быть наследственные факторы или же неблагоприятное внешнее воздействие (УФ-лучи, некачественная декоративная косметика).</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Как действует Карепрост?</h4>
<p>Действующее средство является 0,03% раствор биматопроста. При попадании на кожный участок он активизирует волосяные фолликулы. За счет активизации волосяных фолликул ресницы растут быстрее и дольше остаются живыми, за счет этого достигается их большая длина.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Когда будет виден результат применения?</h4>
<p>Обычно первый результат действия можно увидеть уже через 6-8 недель ежедневного использования. После 16 недели приема эффект достигает своего конечного результата. После достижения конечного результата необходимо применять 1-2 в раза в неделю для поддержания эффекта.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Что если я забыла нанести?</h4>
<p>Не рекомендуется наносить двойную дозу за один раз, это может нанести вред здоровью, просто продолжайте наносить в прежнем режиме.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Что будет, если я перестану принимать?</h4>
<p>В этом случае достигнутый результат подержится некоторые время 4-8 недель и потихоньку спадет. Все вернется в первоначальное состояние.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Какие могут возникнуть побочные эффекты?</h4>
<p>Побочные эффекты проявляются примерно в 4% случаев. Обычно они заключаются в покраснении глаз и зуде. Если обнаружили у себя побочные эффекты, рекомендуем прекратить прием и обратиться за консультацией к врачу.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Я ношу контактные линзы, это как-то помешает?</h4>
<p>Перед применением необходимо снять линзы, а через 15-20 минут после применения их можно одеть снова.</p>

<h4 style="color:#ffffff; font-weight: bold; padding-bottom:9px;">Что будет, если капля Карепроста попадет в глаз?</h4>
<p>Ничего страшного, Карепрост является офтальмологическим препаратом, предназначенный для капания в глаза для снижения внутриглазного давления. Если вдруг это произошло, то никаких особых действий предпринимать необходимости нет.</p>
